package var; //변수는 규격이 중요하다.

public class Start {
	public static void main(String[] args){
		//변수 : 데이터를 담아 컴퓨터와 소통하기 위한 상자
		//변수 제작법
		//규격 이름;
		int a; //int(integer_십진수로된 정수)사이즈로 이름이 a인 상자를 만든것이다. 실수_소숫점이 있는 숫자_잘 쓰지 않는다
		a=10;
		a=99;//상자안에는 물건이 한개만 들어가야한다. 먼저들어있던 건 지우고 마지막에 들어간 물건만 들어갈 수 있다. =은 왼쪽에다 오른쪽 값을 넣는다.
		System.out.println(a);
	}

}
