package bool;

public class Bool {
	public static void main(String[] args) { //시작점
		//boolean : 불리언, 참(true) 또는 거짓(false)을 저장할 수 있는 규격
		boolean b; //참 또는 거짓이 들어가는 작은 상자 b를 만들어라. 
		b=false; //b 상자에 false를 넣어라.
		System.out.println(b); //()안에 들어있는 상자를 콘솔에 적어주세요.
		b=true;
		System.out.println(b);
		
		boolean B=true; //true가 들어있는 상자를 만들어라 선언
		B=false; //B상자를 false로 변경
		
		//위에서 아래로 읽는 내려가는 방법 순차지향
	}
} //종료
