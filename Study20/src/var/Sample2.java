package var;

public class Sample2 {
	public static void main(String[ ] args) {
		//숫자를 저장할 수 있는 변수 두개를 만들어서
		//각각 200과 -4000을 저장한 후
		//console에 출력하시오
		
		int a=200;
		int b=-4000;
		System.out.println(a);
		System.out.println(b);
		
		//저장된 첫번째 수가 두번째 수보다 작은지를 비교하여
		//결과를 변수에 저장하고
		//console에 출력하시오
		
		boolean c= a<b;  //결과물을 변수에 담아서 확인한다
		System.out.println(c);
		
		
		
	}//main종료
}//class종료
